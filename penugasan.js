// =
let a = 2;
a = 4; // a = 4
console.log(a); // 4

// +=
let b = 6;
b += 4; // b = 6 + 4
console.log(b); // 10

// -=
let c = 5;
c -= 3; // c = 5 - 3
console.log(c); // 2

// *=
let d = 5;
d *= 2; // d = 5 * 2
console.log(d); // 10

// /=
let e = 5;
e /= 2; // e = 5 / 2
console.log(e); // 2.5

// %=
let f = 5;
f %= 3; // f = 5 % 3
console.log(f); // 2

// **=
let g = 5;
g **= 3; // g = 5 ** 3
console.log(g); // 125